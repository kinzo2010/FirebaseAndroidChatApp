package kiki.chat.firebase.com.firebasechat.util;

public enum FriendState {

    /**
     * 3 trạng thái của bạn bè
     * FRIEND - là bạn bè
     * WAIT_FOR_ACCEPT - đã gửi yêu cầu và chờ đồng ý
     * FRIEND_REQUEST - người này gửi yêu cầu kết bạn cho mình
     * NOT_IS_FRIEND - hai người không có mối quan hệ nào
     */

    FRIEND, WAIT_FOR_ACCEPT, FRIEND_REQUEST, NOT_IS_FRIEND


}
